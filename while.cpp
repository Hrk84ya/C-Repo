// Including the necessary header file for input-output operations
#include <iostream>
using namespace std;

int main() {
    int num = 10; // Initializing a variable 'num' with the value 10

    // Using a while loop to count down from 10 to 1
    while (num > 0) {
        cout << num << "\n"; // Outputting the value of 'num' followed by a newline
        num--; // Decrementing the value of 'num' by 1 in each iteration
    }

    return 0;
}
