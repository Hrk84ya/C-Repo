// Including the necessary header file for input-output operations
#include <iostream>
using namespace std;

int main() {
    // Initializing an array with 5 integer elements
    int arr[] = {10, 20, 30, 40, 50};

    // Looping through the array elements using a for loop
    for (int i = 0; i < 5; i++) {
        // Printing each element of the array
        cout << arr[i] << "\n";
    }

    return 0;
}
