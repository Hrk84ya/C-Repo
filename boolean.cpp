// Including the necessary header file for input-output operations
#include <iostream>
using namespace std;

int main() {
    // Declaring and initializing boolean variables
    bool isCodingFun = true;   // Variable to indicate if coding is fun
    bool isFishTasty = false;  // Variable to indicate if fish is tasty

    // Outputting the values of the boolean variables
    cout << isCodingFun << "\n";   // Output: 1 (true)
    cout << isFishTasty;           // Output: 0 (false)

    return 0;
}
